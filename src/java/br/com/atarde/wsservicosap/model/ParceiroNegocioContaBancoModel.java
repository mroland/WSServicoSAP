package br.com.atarde.wsservicosap.model;

import java.io.Serializable;

/**
 *
 * @author mroland
 */
public class ParceiroNegocioContaBancoModel implements Serializable {

    //Tabela OCRB
    private Long id;
    private ParceiroNegocioModel parceiroNegocioModel;
    private String codigoBanco;
    private String numeroConta;
    private String digitoConta;  // equivalente a UsrNumber1
    private String numeroAgencia;
    private String digitoAgencia;
    private Integer tipoConta; // equivalente a U_TIPO_CONTA

    public String getCodigoBanco() {
        return codigoBanco;
    }

    public void setCodigoBanco(String codigoBanco) {
        this.codigoBanco = codigoBanco;
    }

    public String getDigitoAgencia() {
        return digitoAgencia;
    }

    public void setDigitoAgencia(String digitoAgencia) {
        this.digitoAgencia = digitoAgencia;
    }

    public String getDigitoConta() {
        return digitoConta;
    }

    public void setDigitoConta(String digitoConta) {
        this.digitoConta = digitoConta;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getNumeroAgencia() {
        return numeroAgencia;
    }

    public void setNumeroAgencia(String numeroAgencia) {
        this.numeroAgencia = numeroAgencia;
    }

    public String getNumeroConta() {
        return numeroConta;
    }

    public void setNumeroConta(String numeroConta) {
        this.numeroConta = numeroConta;
    }

    public ParceiroNegocioModel getParceiroNegocioModel() {
        return parceiroNegocioModel;
    }

    public void setParceiroNegocioModel(ParceiroNegocioModel parceiroNegocioModel) {
        this.parceiroNegocioModel = parceiroNegocioModel;
    }

    public Integer getTipoConta() {
        return tipoConta;
    }

    public void setTipoConta(Integer tipoConta) {
        this.tipoConta = tipoConta;
    }
    
}
