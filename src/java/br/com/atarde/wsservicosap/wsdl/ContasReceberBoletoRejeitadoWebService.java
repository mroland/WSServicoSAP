package br.com.atarde.wsservicosap.wsdl;

import br.com.atarde.wsservicosap.business.ContasReceberBoletoRejeitadoBusiness;
import br.com.atarde.wsservicosap.model.ContasReceberBoletoRejeitadoModel;
import br.com.atarde.wsservicosap.model.ModalidadePagamentoBoletoModel;
import br.com.topsys.exception.TSApplicationException;
import java.util.List;
import javax.jws.Oneway;
import javax.jws.WebService;
import javax.jws.WebMethod;
import javax.jws.WebParam;
import javax.xml.ws.BindingType;

/**
 *
 * @author mroland
 */
@WebService(serviceName = "ContasReceberBoletoRejeitadoWebService")
@BindingType(value="http://java.sun.com/xml/ns/jaxws/2003/05/soap/bindings/HTTP/")
public class ContasReceberBoletoRejeitadoWebService {

    private String retorno;

    /**
     * Operação de serviço web
     */
    @WebMethod(operationName = "pesquisar")
    public List<ContasReceberBoletoRejeitadoModel> pesquisarBoletoRejeitado(@WebParam(name = "contasReceberBoletoRejeitadoModel") ContasReceberBoletoRejeitadoModel model) {

        try {

            return new ContasReceberBoletoRejeitadoBusiness().pesquisar(model);

        } catch (TSApplicationException ex) {

            ex.printStackTrace();

            retorno = ex.getMessage();
        }

        return null;

    }

    /**
     * Operação de serviço web
     */
    @WebMethod(operationName = "retorno")
    public String retorno() {

        return this.retorno;

    }

    /**
     * Operação de serviço web
     */
    @WebMethod(operationName = "excluirContasReceberRejeitado")
    public String excluirContasReceberRejeitado(@WebParam(name = "contasReceberBoletoRejeitadoModel") ContasReceberBoletoRejeitadoModel model) {

        return new ContasReceberBoletoRejeitadoBusiness().excluir(model);
    }

    /**
     * Operação de serviço web
     */
    @WebMethod(operationName = "m33")
    @Oneway
    public void m33(@WebParam(name = "p1") ModalidadePagamentoBoletoModel p1) {
    }

}
