package br.com.atarde.wsservicosap.dao;

import br.com.atarde.wsservicosap.model.NotaFiscalSaidaABModel;
import br.com.atarde.wsservicosap.model.SapNotaFiscalSaidaModel;
import br.com.topsys.database.TSDataBaseBrokerIf;
import br.com.topsys.database.factory.TSDataBaseBrokerFactory;

/**
 *
 * @author mroland
 */
public class SapNotaFiscalSaidaDAO {
    
    public NotaFiscalSaidaABModel obter(SapNotaFiscalSaidaModel model) {

        TSDataBaseBrokerIf broker = TSDataBaseBrokerFactory.getDataBaseBrokerIf();

        broker.setPropertySQL("sapnotafiscalsaidadao.obter", model.getParcelaNotaFiscalSaidaModel().getId());

        return (SapNotaFiscalSaidaModel) broker.getObjectBean(SapNotaFiscalSaidaModel.class, "idExterno","clienteCobrancaModel.id","clienteCobrancaModel.nome","dataDocumento","sequenciaModel.id");

    }

    public NotaFiscalSaidaABModel obterContasReceberInterface(SapNotaFiscalSaidaModel model) {

        TSDataBaseBrokerIf broker = TSDataBaseBrokerFactory.getDataBaseBrokerIf();

        broker.setPropertySQL("sapnotafiscalsaidadao.obterContasReceberInterface", model.getParcelaNotaFiscalSaidaModel().getContasReceberModel().getContasReceberInterfaceModel().getId());

        return (SapNotaFiscalSaidaModel) broker.getObjectBean(SapNotaFiscalSaidaModel.class, "valorLiquido", "idExterno", "origemModel.id");
        
    }
    
    public NotaFiscalSaidaABModel obterContasReceberBoletoRejeitado(SapNotaFiscalSaidaModel model) {

        TSDataBaseBrokerIf broker = TSDataBaseBrokerFactory.getDataBaseBrokerIf();

        broker.setPropertySQL("sapnotafiscalsaidadao.obterContasReceberBoletoRejeitado", model.getParcelaNotaFiscalSaidaModel().getContasReceberModel().getContasReceberBoletoRejeitadoModel().getId());

        return (SapNotaFiscalSaidaModel) broker.getObjectBean(SapNotaFiscalSaidaModel.class, "idExterno", "origemModel.id");
        
    }    


}
